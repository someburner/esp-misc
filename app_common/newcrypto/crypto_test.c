bool testDecryptMultiple(uint8_t * cmsg_plus_nonce, uint16_t inlen)
{
   int r = 0;

   static uint8_t nonce[NONCEBYTES];
   static uint8_t beforenmKey[crypto_box_BEFORENMBYTES];

   uint32_t t0 = 0;
   uint32_t delayt = 0;
   uint8_t testBuf[1024];
   memcpy(nonce, cmsg_plus_nonce+(BOXZEROBYTES+inlen-NONCEBYTES), NONCEBYTES);

   static int x = 0;

   // CRYPTO_DBG("testDecrypt %u bytes..\n", (inlen-NONCEBYTES));
   if (x == 0)
   {
      x++;
      t0 = system_get_time();
      /* NOTE: first auth, takes ~500ms */
      crypto_box_beforenm(beforenmKey, alice_pk , bob_secret);
      delayt = system_get_time() - t0;
      CRYPTO_DBG(" crypto_box_beforenm delay = %u us\n", delayt);
   }

   t0 = system_get_time();
   /* NOTE: after auth - takes ~10ms for an 800 byte payload  */
   r = crypto_box_open_afternm(testBuf, cmsg_plus_nonce, BOXZEROBYTES+(inlen-NONCEBYTES), nonce, beforenmKey);
   delayt = system_get_time() - t0;

   if (!r)
   {
      CRYPTO_DBG(" OK: delay=%uus\n", delayt);

      /* print the first few bytes just to see whats going on */
      if ( ((inlen-NONCEBYTES)-BOXZEROBYTES) > 8)
         testPrintHex(testBuf + ZEROBYTES, 8, false);
      return true;
   } else {
      CRYPTO_DBG("error!\n");
   }

   CRYPTO_DBG(" returned %d\n", r);

   return false;
}


