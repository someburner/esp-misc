static void testPrintHex(uint8_t * buf, int len, bool hex)
{
#ifdef CRYPTO_DEBUG
   int i;
   CRYPTO_DBG("Decrypted[%d]:\n", len);
   if (hex)
   {
      for (i=0; i<len; i++)
         CRYPTO_DBG("0x%02x, ", buf[i]);
   } else {
      for (i=0; i<len; i++)
         CRYPTO_DBG("%c", buf[i]);
   }
   CRYPTO_DBG("\n");
#endif
}

